package com.example.paneninmobile.Home;

public class ItemTerlaris {
    private String title, harga, foto;


    public ItemTerlaris(String title, String harga) {
        this.title = title;
        this.harga = harga;
        this.foto = foto;
    }

    public String getTitle() {
        return title;
    }

    public String getHarga() {
        return harga;
    }

}
